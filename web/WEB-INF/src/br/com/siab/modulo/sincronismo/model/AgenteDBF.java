package br.com.siab.modulo.sincronismo.model;

/**
 * Classe representativa da entidade agente do modelo dbf
 * @version 1.0
 * @author Manoel Bibiano ( bibianofilho@gmail.com )
 */
public class AgenteDBF {
	private String codAgente;
	private String nome;
	
	public String getCodAgente() {
		return codAgente;
	}
	public void setCodAgente(String codAgente) {
		this.codAgente = codAgente;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
}
